
public class ExpressionData {
	String name;
	double x;
	double y;
	double z;
	public ExpressionData(String name, double x, double y, double z){
		this.name = name;
		this.x = x;
		this.y = y;
		this.z = z;
	}
	public String getName(){
		return name;
	}
	public double getX(){
		return x;
	}
	public double getY(){
		return y;
	}
	public double getZ(){
		return z;
	}

}
